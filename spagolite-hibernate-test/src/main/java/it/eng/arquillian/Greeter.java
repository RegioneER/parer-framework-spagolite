package it.eng.arquillian;

import java.io.PrintStream;

/**
 *
 * @author mbertuzzi
 */
public class Greeter {

    public void greet(PrintStream to, String name) {
        to.println(createGreeting(name));
    }

    public String createGreeting(String name) {
        return "Hello, " + name + "!";
    }

}
